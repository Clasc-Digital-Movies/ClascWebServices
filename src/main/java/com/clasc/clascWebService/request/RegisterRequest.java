package com.clasc.clascWebService.request;

public class RegisterRequest {
	
	private String deviceName = null;
	private String deviceID = null;
	private String deviceType = null;
	private String emailTextVal = null;
	private String phoneNumberTextVal = null;
	private String firstNameVal = null;
	private String lastNameVal = null;
	private String genderTextVal = null;
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	public String getDeviceID() {
		return deviceID;
	}
	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getEmailTextVal() {
		return emailTextVal;
	}
	public void setEmailTextVal(String emailTextVal) {
		this.emailTextVal = emailTextVal;
	}
	public String getPhoneNumberTextVal() {
		return phoneNumberTextVal;
	}
	public void setPhoneNumberTextVal(String phoneNumberTextVal) {
		this.phoneNumberTextVal = phoneNumberTextVal;
	}
	public String getFirstNameVal() {
		return firstNameVal;
	}
	public void setFirstNameVal(String firstNameVal) {
		this.firstNameVal = firstNameVal;
	}
	public String getLastNameVal() {
		return lastNameVal;
	}
	public void setLastNameVal(String lastNameVal) {
		this.lastNameVal = lastNameVal;
	}
	public String getGenderTextVal() {
		return genderTextVal;
	}
	public void setGenderTextVal(String genderTextVal) {
		this.genderTextVal = genderTextVal;
	}
	
	
	

}
